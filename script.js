function expandNav() {
	document.querySelector('#mobileNavigation').classList.toggle('expandedNav');
	document.querySelector('#mobileNavigation').classList.toggle('collapsedNav');
}

function toggleCompleteWeb() {
	document.querySelector('#completeWebModal').classList.toggle('activeModal');
    document.querySelector('#completeWebModal').classList.toggle('inactiveModal');
}

